package com.DesignMode.Construction.Adapter;

public class Adapter {
    /**
     * 把一个类的接口变换成客户端所期待的另一种接口，从而使原本因接口不匹配而无法一起工作的两个类能够在一起工作
     * 优点：
     *  1、可以让任何两个没有关联的类一起运行
     *  2、提高了类的复用
     *  3、增加了类的透明度
     *  4、灵活性好
     * 缺点：
     *  1、过多地使用适配器，会让系统非常凌乱
     *  2、由于java只能继承一个类，所以只能适配一个适配者类，且目标类必须是抽象类
     */
}
